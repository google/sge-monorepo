// +build windows

package exec

import (
	"syscall"
)

// HideWindow hides the console window that might otherwise be visible when
// running a command.  Must be called before Start or Run.
func HideWindow(c *Cmd) {
	c.SysProcAttr = &syscall.SysProcAttr{
		CreationFlags: 0x08000000, // CREATE_NO_WINDOW
		HideWindow:    true,
	}
}
